package EntityLayer;

public class highscore {
 private String Name;
 private int id;

 private int time;
 private int moves;
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}
public int getTime() {
	return time;
}
public void setTime(int time) {
	this.time = time;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getMoves() {
	return moves;
}
public void setMoves(int moves) {
	this.moves = moves;
}
 

 
}
